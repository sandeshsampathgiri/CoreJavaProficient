package com.corejava.operators;

public class ArithmeticPrograms {

	public static void main(String[] args) {

		int a = 20;
		int b = 10;
		System.out.println("a=" + a);
		System.out.println("b=" + b);
		System.out.println("a+b=" + (a + b));
		System.out.println("a-b=" + (a - b));
		System.out.println("a*b=" + (a * b));
		System.out.println("a/b=" + (a / b));
		System.out.println("a%b=" + (a % b));

	}

}
