package com.corejava.operators;

public class ArithmeticPrograms3 {

	public static void main(String[] args) {

		double a=15.5,b=20.2;
		System.out.println("a+b="+(a+b));		//35.7
		System.out.println("a-b="+(a-b));		//-4.7
		System.out.println("a*b="+(a*b));		//313.1
		System.out.println("a/b="+(a/b));		//0.7....
		System.out.println("a%b="+(a%b));		//

	}

}
