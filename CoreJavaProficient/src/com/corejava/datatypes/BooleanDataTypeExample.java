package com.corejava.datatypes;

public class BooleanDataTypeExample {

	short a;
	byte b;
	int c;
	float e;
	long f;
	char g;
	boolean h;

	public static void main(String[] args) {
		
		BooleanDataTypeExample bdt = new BooleanDataTypeExample();

		System.out.println("Default value of short datatype is "+bdt.a);
		System.out.println("Default value of byte datatype is "+bdt.b);
		System.out.println("Default value of integer datatype is "+bdt.c);
		System.out.println("Default value of float datatype is "+bdt.e);
		System.out.println("Default value of long datatype is "+bdt.f);
		System.out.println("Default value of char datatype is "+bdt.g);
		System.out.println("Default value of boolean datatype is "+bdt.h);
	}

}
